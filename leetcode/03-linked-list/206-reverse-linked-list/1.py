# Definition for singly-linked list.
class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next


class Solution:
    def reverseList(self, head: ListNode) -> ListNode:
        prev = None
        ptr = head
        while ptr:
            prev, prev.next, ptr = ptr, prev, ptr.next

        return prev
